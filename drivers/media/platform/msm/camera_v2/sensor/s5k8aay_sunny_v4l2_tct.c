{
	pr_info("%s:%d\n", __func__, __LINE__);
	if (s5k8aay_sunny_s_ctrl.pdev) {
		msm_sensor_free_sensor_data(&s5k8aay_sunny_s_ctrl);
		platform_driver_unregister(&s5k8aay_sunny_platform_driver);
	} else
		i2c_del_driver(&s5k8aay_sunny_i2c_driver);
	return;
}

